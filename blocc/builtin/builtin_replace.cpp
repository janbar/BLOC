std::string& REPLACEExpression::literal(Context & ctx) const
{
  const std::string& a = _args[0]->literal(ctx);
  const std::string& b = _args[1]->literal(ctx);
  const std::string& c = _args[2]->literal(ctx);
  std::string& rv = ctx.allocate(std::string());
  size_t p = 0;
  while (p < a.size())
  {
    size_t e = a.find(b, p);
    rv.append(a, p, e - p);
    if (e != std::string::npos)
    {
      rv.append(c);
      p = e + b.size();
    }
    else
      p = e;
  }
  return rv;
}

REPLACEExpression * REPLACEExpression::parse(Parser& p, Context& ctx)
{
  std::vector<Expression*> args;

  try
  {
    TokenPtr t = p.pop();
    if (t->code != '(')
      throw ParseError(EXC_PARSE_FUNC_ARG_NUM_S, KEYWORDS[FUNC_REPSTR]);
    args.push_back(ParseExpression::expression(p, ctx));
    if (!ParseExpression::typeChecking(args.back(), Type::LITERAL, p, ctx))
      throw ParseError(EXC_PARSE_FUNC_ARG_TYPE_S, KEYWORDS[FUNC_REPSTR]);
    t = p.pop();
    if (t->code != Parser::CHAIN)
      throw ParseError(EXC_PARSE_FUNC_ARG_NUM_S, KEYWORDS[FUNC_REPSTR]);
    args.push_back(ParseExpression::expression(p, ctx));
    if (!ParseExpression::typeChecking(args.back(), Type::LITERAL, p, ctx))
      throw ParseError(EXC_PARSE_FUNC_ARG_TYPE_S, KEYWORDS[FUNC_REPSTR]);
    t = p.pop();
    if (t->code != Parser::CHAIN)
      throw ParseError(EXC_PARSE_FUNC_ARG_NUM_S, KEYWORDS[FUNC_REPSTR]);
    args.push_back(ParseExpression::expression(p, ctx));
    if (!ParseExpression::typeChecking(args.back(), Type::LITERAL, p, ctx))
      throw ParseError(EXC_PARSE_FUNC_ARG_TYPE_S, KEYWORDS[FUNC_REPSTR]);
    assertClosedFunction(p, ctx, FUNC_REPSTR);
    return new REPLACEExpression(std::move(args));
  }
  catch (ParseError& pe)
  {
    DBG(DBG_DEBUG, "exception %p at %s line %d\n", &pe, __PRETTY_FUNCTION__, __LINE__);
    for (Expression * e : args)
      delete e;
    throw;
  }
  return nullptr;
}

